def to_secret(origin_text: str) -> str:
    lines = origin_text.splitlines()
    # print(lines)
    num_lines = len(lines)
    half_number = num_lines // 2
    stderr = lines[:half_number]
    stdout = lines[half_number:]
    result = ""
    while len(stdout) != 0 or len(stderr) != 0:
        if len(stdout) > 0:
            result += stdout.pop(0) + "\n"
        if len(stderr) > 0:
            result += stderr.pop(0) + "\n"
        # print(result)
    return result


